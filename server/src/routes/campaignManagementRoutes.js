// POST /:id/cancel — advertiser ends campaign early (no refunds for captured charges; see product policy)
router.post('/:id/cancel', async (req, res) => {
  try {
    const db = getDb();
    const campaign = await verifyCampaignOwnership(req, res);
    if (!campaign) return;

    if (!['active', 'scheduled', 'pending_review'].includes(campaign.status)) {
      return res.status(400).json({ error: 'Campaign cannot be cancelled in its current status' });
    }

    const submission = await db.collection('adSubmissions').findOne({ _id: campaign.submissionId });

    // Release an uncaptured card authorization only (not a refund)
    if (submission?.paymentMode === 'manual_capture' && submission.paymentIntentId) {
      try {
        await stripeService.releaseAuthorization(submission.paymentIntentId, 'Advertiser cancelled before capture');
      } catch (err) {
        console.warn('[campaignManagement] Authorization release failed:', err.message);
      }
    }

    const refundAmountInCents = 0;

    const now = new Date();
    // Cancel the campaign
    await db.collection('adCampaigns').updateOne(
      { _id: campaign._id },
      { $set: { status: 'cancelled', cancelledAt: now, updatedAt: now } }
    );

    // Update submission
    await db.collection('adSubmissions').updateOne(
      { _id: campaign.submissionId },
      { $set: { status: 'cancelled', updatedAt: now } }
    );

    // Remove adTargeting records
    await db.collection('adTargeting').deleteMany({ campaignId: campaign._id });

    const adDisplayName = await resolveAdDisplayName(db, campaign.creativeId);
    notifyAdvertiser(campaign.advertiserId, 'campaign_cancelled', { refundAmount: refundAmountInCents, adDisplayName });

    res.json({ message: 'success', data: { cancelled: true, refundAmountInCents } });
  } catch (err) { res.status(500).json({ error: err.message }); }
});

// GET /:id/refund-estimate — advertiser self-cancellation does not refund (kept for client compatibility)
router.get('/:id/refund-estimate', async (req, res) => {
  try {
    const db = getDb();
    const campaign = await verifyCampaignOwnership(req, res);
    if (!campaign) return;

    res.json({
      message: 'success',
      data: {
        refundAmountInCents: 0,
        remainingDays: 0,
        totalDays: 0,
      },
    });
  } catch (err) { res.status(500).json({ error: err.message }); }
});

module.exports = router;
