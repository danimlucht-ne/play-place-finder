const express = require('express');
const router = express.Router();
const { getDb } = require('../database');
const { ObjectId } = require('mongodb');
const campaignEditService = require('../services/campaignEditService');
const stripeService = require('../services/stripeService');
const { notifyAdvertiser, resolveAdDisplayName } = require('../services/advertiserEmailService');

/** Verify the authenticated user owns the campaign. */
async function verifyCampaignOwnership(req, res) {
  const db = getDb();
  let id;
  try { id = new ObjectId(req.params.id); } catch (_) {
    res.status(400).json({ error: 'Invalid campaign ID' });
    return null;
  }
  const campaign = await db.collection('adCampaigns').findOne({ _id: id });
  if (!campaign) { res.status(404).json({ error: 'Campaign not found' }); return null; }
  const advertiser = await db.collection('advertisers').findOne({ _id: campaign.advertiserId });
  if (!advertiser || advertiser.userId !== req.user.uid) {
    res.status(403).json({ error: 'Forbidden' }); return null;
  }
  return campaign;
}

async function queueCampaignReview(db, campaign, reqUserId, description) {
  const now = new Date();
  if (['scheduled', 'pending_review'].includes(campaign.status)) {
    await db.collection('adCampaigns').updateOne(
      { _id: campaign._id },
      { $set: { status: 'pending_review', updatedAt: now } }
    );
  }
  await db.collection('adSubmissions').updateOne(
    { _id: campaign.submissionId },
    {
      $set: {
        status: 'manual_review',
        updatedAt: now,
        reviewRequestedByUserAt: now,
      },
    }
  );
  await db.collection('reviewFlags').insertOne({
    submissionId: campaign.submissionId,
    flagType: 'user_requested_changes',
    description,
    severity: 'medium',
    autoGenerated: false,
    resolvedAt: null,
    resolvedBy: null,
    createdAt: now,
    createdBy: reqUserId || null,
  });
}

// PUT /:id/creative — edit creative content
router.put('/:id/creative', async (req, res) => {
  try {
    const db = getDb();
    const campaign = await verifyCampaignOwnership(req, res);
    if (!campaign) return;
    const result = await campaignEditService.updateCreative(campaign._id, req.body);
    if (!result.success) return res.status(400).json({ error: result.error });
    if (result.reviewRequired) {
      const description = campaign.status === 'active'
        ? 'User changed a live campaign. Keep the current ad running and review the update before it replaces the approved version.'
        : 'User changed campaign details before launch. Review is required before the ad can run.';
      await queueCampaignReview(db, campaign, req.user?.uid, description);
    }
    res.json({
      message: result.reviewRequired && campaign.status === 'active'
        ? 'success_pending_review'
        : 'success',
    });
  } catch (err) { res.status(500).json({ error: err.message }); }
});

